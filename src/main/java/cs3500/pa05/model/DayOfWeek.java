package cs3500.pa05.model;

/**
 * Enumeration of possible days of the week.
 */
public enum DayOfWeek {
  /**
   * Represents Sunday
   */
  SUNDAY,
  /**
   * Represents Monday
   */
  MONDAY,
  /**
   * Represents Tuesday
   */
  TUESDAY,
  /**
   * Represents Wednesday
   */
  WEDNESDAY,
  /**
   * Represents Thursday
   */
  THURSDAY,
  /**
   * Represents Friday
   */
  FRIDAY,
  /**
   * Represents Saturday
   */
  SATURDAY
}
